// ========== i18n Engine ==========
const LANG_LABELS = { en: 'EN', ko: 'KO', ja: 'JA', zh: 'ZH' };

function getCurrentLang() {
  return localStorage.getItem('apl-lang') || 'en';
}

function getTranslation(key) {
  const lang = getCurrentLang();
  return (translations[lang] && translations[lang][key]) || translations.en[key] || key;
}

function applyTranslations(lang) {
  const t = translations[lang] || translations.en;

  // Apply data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) el.innerHTML = t[key];
  });

  // Apply data-i18n-placeholder
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key]) el.placeholder = t[key];
  });
}

function setLang(lang) {
  localStorage.setItem('apl-lang', lang);
  document.documentElement.lang = lang;

  // Update label
  const label = document.getElementById('langLabel');
  if (label) label.textContent = LANG_LABELS[lang] || lang.toUpperCase();

  // Update dropdown active state
  document.querySelectorAll('.lang-dropdown button').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  // Close dropdown
  const dropdown = document.getElementById('langDropdown');
  if (dropdown) dropdown.classList.remove('open');

  applyTranslations(lang);

  // Update phone/email format based on language
  if (typeof updatePhoneEmailByLang === 'function') {
    updatePhoneEmailByLang(lang);
  }
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
  const lang = getCurrentLang();
  setLang(lang);
});
